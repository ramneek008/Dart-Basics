import 'dart:math';

main(){

  //Operators

  var num1 = 10;
  var num2 = 5;

  var add = num1 + num2;
  var sub = num1 - num2;
  var mul = num1 * num2;
  var div = num1 / num2;
  var mod = num1 % num2;

  print("num1 + num2 = ${add}");
  print("num1 - num2 = ${sub}");
  print("num1 * num2 = ${mul}");
  print("num1 / num2 = ${div}");
  print("num1 % num2 = ${mod}");

  var edge = 5;
  var sa = 6 * pow(edge,2);
  print("Surface Area : ${sa}");

}